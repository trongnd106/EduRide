 * @method static \Illuminate\Database\Eloquent\Builder|User whereUpdatedAt($value)
 * @method static \Illuminate\Database\Eloquent\Builder|User whereUsername($value)
 * @mixin \Eloquent
 */
class Admin extends Authenticatable
{
    use HasApiTokens, HasFactory, Notifiable, HasRoles, SoftDeletes;

    public $table = "admins";
    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'username',
        'email',
        'password',
        'first_name',
        'last_name',
        'spir_calendar_link',
        'last_login',
        'status'
    ];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'password',
        'remember_token',
    ];

    /**
     * The attributes that should be cast to native types.
     *
     * @var array
     */
    protected $casts = [
        'email_verified_at' => 'datetime',
    ];

    protected $appends = ['is_online', 'role_name'];

    protected function serializeDate(\DateTimeInterface $date): string
    {
        return $date->format('Y-m-d H:i:s');
    }

    public function findForPassport($email)
    {
        return $this->where('email', $email)->where('status', AppConst::STATUS_ACTIVE)->first();
    }

    public function guardName(): string
    {
        return AppConst::ADMIN_GUARD_NAME;
    }

    public function roles(): BelongsToMany
    {
        return $this->morphToMany(
            config('permission.models.role'),
            'model',
            config('permission.table_names.model_has_roles'),
            config('permission.column_names.model_morph_key'),
            'role_id',
            'id'
        );
    }

    public function comments()
    {
        return $this->morphMany(ConsultationMemoComment::class, 'commenter');
    }

    public function getRoleNameAttribute()
    {
        return $this->roles()->first()->name ?? null;
    }

    public function getRoleIdAttribute()
    {
        return $this->roles()->first()->id;
    }

    protected function isOnline(): Attribute
    {
        return Attribute::make(
            get: fn() => $this->last_access && Carbon::parse($this->last_access)->gt(now()->subMinutes(AppConst::LAST_ACCESS_THRESHOLD))
        );
    }

    public function userConcierges()
    {
        return $this->hasMany(UserConcierge::class, 'concierge_id');
    }
}
